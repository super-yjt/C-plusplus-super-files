#include <iostream>
int main(void)
{
    using namespace std;
    // goto 跳转
    cout << "1.xxxxx"<<"\n";
    cout << "2.xxxxx"<<"\n";
    cout << "3.xxxxx"<<"\n";
    goto FLAG;
    cout << "4.xxxxx"<<"\n";
    cout << "5.xxxxx"<<"\n";
    cout << "6.xxxxx"<<"\n";
    cout << "7.xxxxx"<<"\n";
    FLAG:
    cout << "8.xxxxx"<<"\n";
    cout << "9.xxxxx"<<"\n";
}